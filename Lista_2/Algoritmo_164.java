package Lista_2;

public class Algoritmo_164 {
  public static void main(String[] args) {
    //Com For
    System.out.println("Comece");
    for (int i = 0; i < 50000; i++) {
    }
    System.out.println("Pare");
    int i =0;
    //com while
    System.out.println("Comece");
    while(i < 50000) {
      i++;
    }
    System.out.println("Pare");
    //com do while
    i =0;
    System.out.println("Comece");
    do{
      i++;
    }while(i < 50000);
    System.out.println("Pare");
  }
}
